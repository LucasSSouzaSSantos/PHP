 * Aula 02: O que é um Objeto?
 *   > Definição: Coisa materail ou abstrata que pode ser 
 * percebida pelos sentidos e descrita por meio das suas 
 * características, comportamentos e estdo atual.
 * 
 *  > Objeto: Caneta Azul
 *    Para a POO a caneta de cor azul é um objeto, equanto o 
 * molde é considerada uma classe.
 *    Uma classe tem que sempre responder a três perguntas
 *      - Coisas que eu tenho ?(Atributos)
 *          - Modelo
 *          - cor
 *          - Ponta
 *          - Carga
 *          - Tampada
 *      - Coisas que eu faço ? (Métodos)
 *          - Escrever
 *          - Rabiscar
 *          - Pintar
 *          - Tampar
 *          - Destampar
 *      - Como eu estou agora ? (Estado)
 * + -------------------------------+
 * | > Classe Caneta                |
 * +--------------------------------+
 * |  modelo:     Caractere         |
 * |  cor:        Caractere         |
 * |  ponta:      Real              |
 * |  carga:      Inteiro           |
 * |  tampada:    Logico            |
 * +--------------------------------+
 * |    Metodo rabiscar()           |
 * |        se (tampada) então      |
 * |            Escreva("Erro")     |
 * |        senão                   |
 * |                                |
 * |        fimSe                   |
 * |                                |    
 * |    fimMetodo                   |    
 * |                                |                
 * |    Metodo tampar()             |                
 * |                                |                        
 * |    fimMetodo                   |            
 * |                                |                
 * |                                |                            
 * |  > FimClasse                   |            
 * +--------------------------------+
 * 
